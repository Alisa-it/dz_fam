import math

def sqrt(x):
    float(x)
    return(math.sqrt(x))

def sin(x):
    float(x)
    return(math.sin(math.radians(x)))

def arccos(x):
    float(x)
    return(math.acos(x))

def cos(x):
    float(x)
    return(math.cos(math.radians(x)))

def tg(x):
    float(x)
    return(math.tan(math.radians(x)))

def grad(x):
    float(x)
    return(x*180/3.1416)